char	*ft_strchr(const char *s, int c)
{	
	int	i;

	i = 0;
	if (c == '\0')
		return (&s[ft_strlen(s) + 1]);
	while (s[i])
	{
		if (c == s[i])
			return (s);
		i++;
	}
	return (0);
}
